def main():
    conn = get_conn()
    cur = conn.cursor()
    t0 = time.time()

    print("=" * 60)
    print("Building custom surname ethnicity groups")
    print("=" * 60)

    # 1. Lookup table
    print("\nStep 1: Building lookup table...")
    cur.execute("DROP TABLE IF EXISTS ref_custom_surnames")
    cur.execute("""
        CREATE TABLE ref_custom_surnames (
            surname   VARCHAR(100) NOT NULL,
            eth_group VARCHAR(30)  NOT NULL,
            PRIMARY KEY (surname, eth_group),
            INDEX idx_surname (surname)
        ) ENGINE=InnoDB CHARSET=utf8mb4
    """)
    total_inserted = 0
    for group, names in SURNAMES.items():
        deduped = list(set(n.upper().strip() for n in names))
        rows = [(n, group) for n in deduped]
        cur.executemany(
            "INSERT IGNORE INTO ref_custom_surnames (surname, eth_group) VALUES (%s,%s)", rows)
        print(f"  {group:<20} {len(deduped):>4} surnames")
        total_inserted += len(deduped)
    conn.commit()
    print(f"  Total: {total_inserted} entries")

    # 2. Add / reset flag columns
    print("\nStep 2: Adding flag columns to voter_file...")
    cur.execute("SHOW COLUMNS FROM voter_file")
    existing = {r[0] for r in cur.fetchall()}
    groups = list(SURNAMES.keys())
    for group in groups:
        col = f"eth_{group}"
        if col not in existing:
            cur.execute(f"ALTER TABLE voter_file ADD COLUMN {col} TINYINT(1) DEFAULT 0")
            print(f"  Added {col}")
        else:
            cur.execute(f"UPDATE voter_file SET {col} = 0")
            print(f"  Reset {col}")
    # Drop old eth_russian if present
    if "eth_russian" in existing:
        cur.execute("ALTER TABLE voter_file DROP COLUMN eth_russian")
        print("  Dropped eth_russian (replaced by eth_eastern_european)")
    conn.commit()

    # 3. Populate flags
    print("\nStep 3: Flagging voters by surname...")
    for group in groups:
        col = f"eth_{group}"
        t = time.time()
        cur.execute(f"""
            UPDATE voter_file f
            JOIN ref_custom_surnames s
              ON s.surname = UPPER(f.LastName)
             AND s.eth_group = %s
            SET f.{col} = 1
        """, (group,))
        matched = cur.rowcount
        conn.commit()
        print(f"  {group:<20} {matched:>8,} voters flagged ({time.time()-t:.1f}s)")

    # 4. Summary
    print("\nStep 4: Statewide summary...")
    selects = ", ".join(f"SUM(eth_{g})" for g in groups)
    cur.execute(f"SELECT {selects}, COUNT(*) FROM voter_file")
    r = cur.fetchone()
    total = int(r[-1])
    for i, g in enumerate(groups):
        n = int(r[i])
        print(f"  {g:<20} {n:>8,}  ({n*100.0/total:.2f}%)")

    print("\nLD 63 sample:")
    cur.execute(f"SELECT {selects}, COUNT(*) FROM voter_file WHERE LDName='63'")
    r = cur.fetchone()
    total = int(r[-1])
    for i, g in enumerate(groups):
        n = int(r[i])
        print(f"  {g:<20} {n:>6,}  ({n*100.0/total:.1f}%)")

    cur.close(); conn.close()
    print(f"\nDone in {(time.time()-t0)/60:.1f} min")

if __name__ == "__main__":
    main()
